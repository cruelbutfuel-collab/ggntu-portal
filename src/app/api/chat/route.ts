/* ── HTML formatter (like original server.js) ── */
function formatReply(text: string): string {
  const lines = text.split(/\r?\n/)
  let html = ''
  let inList = false
  for (const line of lines) {
    const t = line.trim()
    const isBullet = /^[-*+] /.test(t)
    if (isBullet) {
      if (!inList) { html += '<ul class="br">'; inList = true }
      html += '<li>' + t.slice(2).trim() + '</li>'
    } else {
      if (inList) { html += '</ul>'; inList = false }
      if (t === '') { if (html && !html.endsWith('<br>')) html += '<br>' }
      else html += (html && !html.endsWith('>') ? '<br>' : '') + t
    }
  }
  if (inList) html += '</ul>'
  return html.replace(/<br><br>/g, '<br>')
}

/* ── Local fallback (no API key) ─────────────── */
function guessIntent(text: string): string {
  const t = text.toLowerCase()
  if (/(колледж|спо|9 ?класс)/.test(t))                         return 'fspo'
  if (/(специал|направлен|факульт|институт)/.test(t))           return 'specialties'
  if (/(экзам|егэ|предмет)/.test(t))                            return 'exams'
  if (/(бюджет|плат)/.test(t))                                  return 'budget'
  if (/(документ|снилс|паспорт|аттестат)/.test(t))             return 'docs'
  if (/(срок|дата|когда|при[её]м|приема)/.test(t))              return 'dates'
  return 'default'
}

/* ── Route handler ───────────────────────────── */
export async function POST(req: NextRequest) {
  const body = await req.json()
  const messages: { role: string; content: string }[] = body.messages ?? []

  /* No API key → local fallback */
  if (!process.env.GROQ_API_KEY) {
    const last = messages[messages.length - 1]?.content ?? ''
    const key = guessIntent(last)
    const r = BOT_REPLIES[key] ?? BOT_REPLIES.default
    return NextResponse.json({ reply: r.text })
  }

  /* Groq API */
  try {
    const res = await fetch('https://api.groq.com/openai/v1/chat/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${process.env.GROQ_API_KEY}`,
      },
      body: JSON.stringify({
        model: 'llama-3.1-8b-instant',
        max_tokens: 800,
        messages: [{ role: 'system', content: SYSTEM }, ...messages],
      }),
    })

    if (!res.ok) {
      const err = await res.json() as { error?: { message?: string } }
      throw new Error(err.error?.message ?? `HTTP ${res.status}`)
    }

    const data = await res.json() as { choices: { message: { content: string } }[] }
    let raw = data.choices[0].message.content
    raw = raw.replace(/<think>[\s\S]*?<\/think>\s*/g, '')
    const clean = raw.replace(/[^\x20-\x7EЀ-ӿ–—\s]/g, '')
    return NextResponse.json({ reply: formatReply(clean) })
  } catch (e) {
    console.error('Groq error:', (e as Error).message)
    return NextResponse.json({ reply: 'Извини, не смогла ответить. Попробуй ещё раз.' })
  }
}
